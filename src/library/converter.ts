export const volumeOunceToMilliliter = (value: number) => {
  return value / 29.574;
};

export const volumeMilliliterToOunce = (value: number) => {
  return value * 29.574;
};

export const weightKilogramsToPounds = (value: number) => {
  return value * 2.205;
};

export const weightPoundsToKilograms = (value: number) => {
  return value / 2.205;
};
